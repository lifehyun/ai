package javaBasic3.ch06.sec08;

public class 숙제5중한개 {
//String 타입 변환하기
	public static void main(String[] args) {
	
	int x;	  			//변수 x선언하기	
	x = 100;			//변수 x에 100값을 저장
	int y = 200;		//변수 y에 200값을 저장
	int result  = x+y; //변수 result를 선언하고 변수 x와 y를 더한 값을 저장
	System.out.println("x와 y의 더한값이"+" "+ result+"이다");	
	}
	
	

}
